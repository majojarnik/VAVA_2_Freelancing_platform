    private void btnAddMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseReleased
        int[] specRows = tblSpec.getSelectedRows();
        
        if (specRows.length == 0){
            JOptionPane.showMessageDialog(rootPane, "Vyberte zamestnanca, ktorého chcete priradiť k pracovnej ponuke.", "Nie je vybraný zamestnanec", JOptionPane.ERROR_MESSAGE);
        }
        else if (specRows.length > offer.getNumEmp()){
            if (offer.getNumEmp() == 1)
                JOptionPane.showMessageDialog(rootPane, "Pozícia je iba pre 1 zamestnanca.", "Príliš veľa zamestnancov", JOptionPane.ERROR_MESSAGE);
            else
                JOptionPane.showMessageDialog(rootPane, "Pozícia je iba pre " + offer.getNumEmp() + "zamestnancov.", "Príliš veľa zamestnancov", JOptionPane.ERROR_MESSAGE);
        }
        else{
            List<Specialist> forLease = new ArrayList<Specialist>();
            for (int i = 0; i < specRows.length; i++) {
                forLease.add(specs.get(specRows[i]));
            }
            
            LeaseController leaCon = new LeaseController();
            leaCon.addLease(offer.getEmployer(), forLease);
            
            offer.setNumEmp(offer.getNumEmp() - specRows.length);
            if (offer.getNumEmp() == 1)
                slblDesc.setText("Spoločnosť " + offer.getEmployer().getName() + " hľadá do svojho tímu nového kolegu.");
            else
                slblDesc.setText("Spoločnosť " + offer.getEmployer().getName() + " hľadá do svojho tímu " + offer.getNumEmp() + " nových kolegov.");
            
            parent.initLeaseTable();
            parent.initSpecTables();
            tblSpec.setModel(initSpecTableModel());
                   
        }
        
    }//GEN-LAST:event_btnAddMouseReleased

