    public ShowOffer(JobOffer offer, MainWindow parent) {
        initComponents();
        
        this.offer = offer;
        this.parent = parent;
        
        slblType.setText(offer.getType());
        
        if (offer.getNumEmp() == 1)
            slblDesc.setText("Spoločnosť " + offer.getEmployer().getName() + " hľadá do svojho tímu nového kolegu.");
        else
            slblDesc.setText("Spoločnosť " + offer.getEmployer().getName() + " hľadá do svojho tímu " + offer.getNumEmp() + " nových kolegov.");

        slblManDay.setText(String.valueOf(offer.getManDay()));
        slblEdu.setText(offer.getEducation().toString());
        slblExper.setText(String.valueOf(offer.getExperience()));
        if (Boolean.logicalOr(offer.getType().equals("Programátor") , offer.getType().equals("Administrátor"))){
            slblArea.setText(offer.getArea());
        }
        else if (offer.isNBU()){
            slblAreaOrNBU.setText("Je potrebné byť auditor kybernetickej bezpečnosti");
            slblArea.setVisible(false);
        }
        
        if (! offer.getType().equals("Administrátor"))
            lblPlatform.setVisible(false);
        else
            slblPlatform.setText(offer.getPlatform());
        
        for (String certificate : offer.getCertificates()) {
            JLabel lblNew = new JLabel();
            lblNew.setText(certificate);
            lblNew.setFont(new Font("sansserif", Font.PLAIN, 14));
            spnlCerts.add(lblNew);
        }
        
        for (Specialist spec : Data.getAllSpecialists()) {
            if (spec.goodForPosition(offer))
                specs.add(spec);
        }
        
        tblSpec.setModel(initSpecTableModel());
        
    }
    
    private TableModel initSpecTableModel(){
        List<String[]> values = new ArrayList<String[]>();
        List<String> columns = new ArrayList<String>();;
                  
        for (Specialist spec: specs){
            if (columns.isEmpty())
                columns = spec.columnsTable();
            if (spec.isFree())
                values.add(spec.rowsTable());
        }
        
        
        TableModel tableModel = new DefaultTableModel(values.toArray(new Object[][] {}), columns.toArray());
        return tableModel;        
    }

